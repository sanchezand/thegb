OPCodeFn* opMap[0xFF];
OPCodeFn* opMapCB[0xFF];

OPCode executeCode(Boy *gb, uint8_t code) {
	OPCode opcode = getOpFunction(code)(gb);
	return opcode;
}

OPCodeFn* getOpFunction(uint8_t code) {
	OPCodeFn* fn = opMap[code];
	if (fn == nullptr) {
		return &opDummy;
	}
	return fn;
}

OPCode opDummy(Boy* gb) {
	printf("Missing implementation: 0x%02x\n", gb->getCurrentInstruction());
	return OPCode(0x00, "NO DUMMY", 0);
}

OPCode op0xCB(Boy* gb) {
	// GET CB FUNCTION
	uint8_t n = gb->getNextInstruction();
	return opMapCB[n](gb);
}

